# B.1 Task 1
def relu(z):
    res = np.maximum(0, z)
    return res

# B.1 Task 2
def relu_derivative(z):
    res = (z > 0).astype(float)
    return res

# B.1 Task 3
def sigmoid(z):
    z = np.clip(z, -50, 50)
    res = 1 / (1 + np.exp(-z))
    return res

# B.1 Task 4
def mse_loss(y_true, y_pred):
    res = np.mean((y_true - y_pred)**2)
    return res

