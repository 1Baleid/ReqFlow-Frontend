def train_val_split(x, y, val_ratio=0.2, seed=None):

    assert len(x) == len(y), "x and y must have same number of samples"

    N = len(x)

    # random seed
    if seed is not None:
        np.random.seed(seed)

    # Shuffle indices
    indices = np.random.permutation(N)

    # Split point
    val_size = int(N * val_ratio)

    val_idx = indices[:val_size]
    train_idx = indices[val_size:]

    # Make splits
    x_val = x[val_idx]
    y_val = y[val_idx]

    x_train = x[train_idx]
    y_train = y[train_idx]

    return x_train, y_train, x_val, y_val


