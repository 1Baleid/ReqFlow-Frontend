def init_params(input_dim=5, hidden_dim=4, output_dim=1):
    # Small random weights, zero biases
    W1 = 0.1 * np.random.randn(input_dim, hidden_dim)
    b1 = np.zeros((1, hidden_dim))
    W2 = 0.1 * np.random.randn(hidden_dim, output_dim)
    b2 = np.zeros((1, output_dim))

    return {"W1": W1, "b1": b1, "W2": W2, "b2": b2}

"""#### Part B: Step 3 - Implement Forward Pass [10 Points]

Complete the code in the cell below
"""

def forward_pass(X, params): # Do not change this line, do not change the variable names
    W1, b1 = params["W1"], params["b1"] # Do not change this line
    W2, b2 = params["W2"], params["b2"] # Do not change this line


    z1 = X @ W1 + b1          # B.3 Task 1
    a1 = relu(z1)             # B.3 Task 2
    z2 = a1 @ W2 + b2         # B.3 Task 3
    y_hat = sigmoid(z2)       # B.3 Task 4

    cache = {"X": X, "z1": z1, "a1": a1, "z2": z2, "y_hat": y_hat} # Do not change this line

    return y_hat, cache # Do not change this line

"""#### Part B: Step 4 - Implement Backpropagation using Mean Squared Error (MSE) loss [10 Points]

Complete the code in the cell below (see TO DO lines)
"""

def backward_pass(y_true, params, cache): # Do not change this line, do not change variable names
    W2 = params["W2"]

    X   = cache["X"]
    z1  = cache["z1"]
    a1  = cache["a1"]
    y_hat = cache["y_hat"]

    N = X.shape[0]


    d_yhat = (2 / N) * (y_hat - y_true)            # B.4 Task 1
    d_z2 = d_yhat * (y_hat * (1 - y_hat))          # B.4 Task 2
    dW2 = a1.T @ d_z2                              # B.4 Task 3

    db2 = np.sum(d_z2, axis=0, keepdims=True)  # Do not change this line

    # Backprop into hidden layer
    d_a1 = d_z2 @ W2.T                         # (N, H) # Do not change this line
    d_z1 = d_a1 * relu_derivative(z1)          # (N, H) # Do not change this line

    # z1 = X @ W1 + b1
    dW1 = X.T @ d_z1                           # (5, H)
    db1 = np.sum(d_z1, axis=0, keepdims=True)  # (1, H)

    grads = {"dW1": dW1, "db1": db1, "dW2": dW2, "db2": db2}
    return grads

"""#### Part B: Step 5 - Training loop (full-batch gradient descent) [10 Points]

* Complete the code in the cell below (see TO DO lines)
* Do not change any other lines of code
"""

# This function has the training code

def train_nn(X, y, lr=0.1, epochs=2000, print_every=200):
    params = init_params(input_dim=X.shape[1], hidden_dim=4, output_dim=1)
    losses = []

    for epoch in range(epochs):
        # Forward
        y_hat, cache = forward_pass(X, params)

        # Loss
        loss = mse_loss(y, y_hat)
        losses.append(loss)

        # Backward
        grads = backward_pass(y, params, cache)

        # Gradient descent update
        params["W1"] = params["W1"] - lr * grads["dW1"]  # B.5 Task 1
        params["b1"] = params["b1"] - lr * grads["db1"]  # B.5 Task 2
        params["W2"] = params["W2"] - lr * grads["dW2"]  # B.5 Task 3
        params["b2"] = params["b2"] - lr * grads["db2"]  # B.5 Task 4
        if epoch % print_every == 0 or epoch == epochs - 1:
            print(f"Epoch {epoch:4d} | MSE loss = {loss:.6f}")

    return params, np.array(losses) # Do not change

